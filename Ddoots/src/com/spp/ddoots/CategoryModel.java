package com.spp.ddoots;

public class CategoryModel {
	
		int id;
		String name;
		
		public CategoryModel(int id, String name) {
			super();
			this.id = id;
			this.name = name;
			
			
		}
		public int getId() {
			return id;
		}
		
		public String getName() {
			return name;
		}

		
}
